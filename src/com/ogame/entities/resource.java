package com.ogame.entities;

public class resource {

	private int id;
	private int uid;
	private int metal;
	private int beton;
	private int fuel;
	private int metalAdd;
	private int betonAdd;
	private int fuelAdd;
	private long time;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getMetal() {
		return metal;
	}
	public void setMetal(int metal) {
		this.metal = metal;
	}
	public int getBeton() {
		return beton;
	}
	public void setBeton(int beton) {
		this.beton = beton;
	}
	public int getFuel() {
		return fuel;
	}
	public void setFuel(int fuel) {
		this.fuel = fuel;
	}
	public int getMetalAdd() {
		return metalAdd;
	}
	public void setMetalAdd(int metalAdd) {
		this.metalAdd = metalAdd;
	}
	public int getBetonAdd() {
		return betonAdd;
	}
	public void setBetonAdd(int betonAdd) {
		this.betonAdd = betonAdd;
	}
	public int getFuelAdd() {
		return fuelAdd;
	}
	public void setFuelAdd(int fuelAdd) {
		this.fuelAdd = fuelAdd;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	
}
